package Bucles;

/*
 * El bucle for se utiliza principalmente cuando ya sabemos el numero de iteraciones (repeticiones) necesarias
 * que se van a hacer
 * */

public class EjemploBucleFor {
    public static void main(String[] args) {
        for (int i = 1; i < 11; i++) {
            System.out.print(i + ",");
        }
    }
}
